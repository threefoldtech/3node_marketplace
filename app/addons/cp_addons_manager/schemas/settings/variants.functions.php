use Tygh\Registry;

function fn_settings_variants_addons_cp_addons_manager_use_logs()
{
    $object_variants = array();
    $sections = fn_get_schema('cp_logs', 'sections', 'php', true);

    foreach ($sections as $key => $section) {
        $object_variants[$key] = !empty($section['title']) ? $section['title'] : __($key);
    }

    return $object_variants;
}